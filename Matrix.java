public class Matrix{

    public static void main(String[] args){

        int[][] matrix = new int[10][10];
        int k =0;
        for(int i=0;i<10;i++){
            for(int j=0;j<10;j++)
                System.out.printf("%3d ",matrix[i][j] = ++k);

            
            System.out.println(" ");
        }
        
    }
}